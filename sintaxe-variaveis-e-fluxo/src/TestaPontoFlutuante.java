
public class TestaPontoFlutuante {
	
	public static void main(String[] args) {
		
		double salario;
		salario = 1250.70;
		
		System.out.println("Meu salário e " + salario);
		
		double divisao = 3.14 / 2;
		System.out.println("A divisao (double 0.x) e " + divisao);
		
		int novaDivisao = 5 / 2;
		System.out.println("A nova divisao com INT e " + novaDivisao);
		
		double maisUmaDivisao = 5.0 / 2;
		System.out.println("A outra divisao e " + maisUmaDivisao);
		
	}

}
